#15) Escreva um programa que converta uma temperatura digitando em graus Celsius e converta para graus Fahrenheit

print("RESOLUÇÃO DA 15")

Graus_Celsius = int(input("Digite a temperatura em Celsius"))

Fareinheit = (9 * Graus_Celsius /5) + 32

print("{}°C equivale a {} °F".format(Graus_Celsius, Fareinheit))

